class CubeWriter:
    @staticmethod
    def write(filepath, atoms_sym, atoms_coords, origin, vectors, data, comment=""):
        """
        Write a .cube file.
        atoms_sym: list of atomic symbols (str) or atomic numbers (int)
        atoms_coords: list/array of (x,y,z) in Angstrom
        origin: (x,y,z) of grid origin in Bohr
        vectors: 3x3 array of grid vectors in Bohr
        data: 3D numpy array of values
        """
        dirname = os.path.dirname(filepath)
        if dirname and not os.path.exists(dirname):
            try:
                os.makedirs(dirname, exist_ok=True)
            except Exception as e:
                print(f"Error creating directory {dirname}: {e}")

        nx, ny, nz = data.shape
        n_atoms = len(atoms_sym)

        try:
            from rdkit import Chem
            pt = Chem.GetPeriodicTable()
            def to_z(s):
                return pt.GetAtomicNumber(s) if isinstance(s, str) else int(s)
        except Exception:
            def to_z(s):
                return 0 if isinstance(s, str) else int(s)

        BOHR_TO_ANG = 0.529177249
        ANG_TO_BOHR = 1.0 / BOHR_TO_ANG
        coords_bohr = np.array(atoms_coords) * ANG_TO_BOHR

        with open(filepath, "w") as f:
            f.write(f"ORCA Analyzer Cube File: {comment}\n")
            f.write("Generated by MoleditPy ORCA Plugin\n")
            f.write(
                f"{n_atoms:5d} {origin[0]:12.6f} {origin[1]:12.6f} {origin[2]:12.6f}\n"
            )
            f.write(
                f"{nx:5d} {vectors[0, 0]:12.6f} {vectors[0, 1]:12.6f} {vectors[0, 2]:12.6f}\n"
            )
            f.write(
                f"{ny:5d} {vectors[1, 0]:12.6f} {vectors[1, 1]:12.6f} {vectors[1, 2]:12.6f}\n"
            )
            f.write(
                f"{nz:5d} {vectors[2, 0]:12.6f} {vectors[2, 1]:12.6f} {vectors[2, 2]:12.6f}\n"
            )
            for i in range(n_atoms):
                z_no = to_z(atoms_sym[i])
                f.write(
                    f"{z_no:5d} {float(z_no):12.6f} "
                    f"{coords_bohr[i][0]:12.6f} {coords_bohr[i][1]:12.6f} {coords_bohr[i][2]:12.6f}\n"
                )
            vals = data.flatten()
            for i, v in enumerate(vals):
                f.write(f"{v:13.5E}")
                if (i + 1) % 6 == 0:
                    f.write("\n")
            if len(vals) % 6 != 0:
                f.write("\n")


class BasisSetEngine:
    """
    GTO-based Basis Set evaluator backed by the Rust extension.
    Accepts the same shell dicts as the original Python implementation.
    """

    def __init__(self, shells):
        """
        shells: list of dicts — each:
          {'type': int, 'center': [x,y,z] Bohr, 'exps': array, 'coeffs': array}
        """
        rust_shells = [
            {
                "type": int(sh["type"]),
                "center": [float(v) for v in sh["center"]],
                "exps": [float(v) for v in sh["exps"]],
                "coeffs": [float(v) for v in sh["coeffs"]],
            }
            for sh in shells
        ]
        self._engine = _rs.BasisSetEngineRust(rust_shells)
        self.n_basis = self._engine.n_basis

    def evaluate_mo_on_grid(self, mo_idx, grid_coords, mo_coeffs_all):
        """
        Evaluate MO on grid.
        mo_coeffs_all: 1D array of all MO coefficients (must match n_basis)
        grid_coords: (N, 3) array in Bohr
        Returns (N,) numpy array of phi values.
        """
        if len(mo_coeffs_all) < self.n_basis:
            raise ValueError(
                f"Not enough coefficients. Needed {self.n_basis}, got {len(mo_coeffs_all)}"
            )
        grid_flat = grid_coords.ravel().tolist()
        result = self._engine.evaluate_mo_on_grid(grid_flat, list(mo_coeffs_all))
        return np.array(result)


class CalcWorker(QThread):
    progress_sig = pyqtSignal(int)
    finished_sig = pyqtSignal(bool, str)

    def __init__(
        self,
        engine,
        mo_idx,
        n_points,
        margin,
        atoms_sym,
        atoms_coords,
        mo_coeffs,
        output_path,
    ):
        super().__init__()
        self.engine = engine
        self.mo_idx = mo_idx
        self.n_points = n_points
        self.margin = margin
        self.atoms_sym = atoms_sym
        self.atoms_coords = atoms_coords
        self.mo_coeffs = mo_coeffs
        self.output_path = output_path
        self._is_cancelled = False

    def run(self):
        try:
            BOHR_TO_ANG = 0.529177249
            ANG_TO_BOHR = 1.0 / BOHR_TO_ANG

            coords_bohr = np.array(self.atoms_coords) * ANG_TO_BOHR
            margin_bohr = self.margin * ANG_TO_BOHR

            min_c = np.min(coords_bohr, axis=0) - margin_bohr
            max_c = np.max(coords_bohr, axis=0) + margin_bohr
            span = max_c - min_c

            nx = ny = nz = self.n_points
            dx = span[0] / (nx - 1)
            dy = span[1] / (ny - 1)
            dz = span[2] / (nz - 1)

            x = np.linspace(min_c[0], max_c[0], nx)
            y = np.linspace(min_c[1], max_c[1], ny)
            z = np.linspace(min_c[2], max_c[2], nz)

            X, Y, Z = np.meshgrid(x, y, z, indexing="ij")
            grid_points = np.column_stack([X.ravel(), Y.ravel(), Z.ravel()])

            n_total = grid_points.shape[0]
            chunk_size = 50000
            result_flat = np.zeros(n_total)

            for i, start in enumerate(range(0, n_total, chunk_size)):
                if self._is_cancelled:
                    return
                end = min(start + chunk_size, n_total)
                chunk_pts = grid_points[start:end]

                val = self.engine.evaluate_mo_on_grid(
                    self.mo_idx, chunk_pts, self.mo_coeffs
                )
                result_flat[start:end] = val

                pct = int((end / n_total) * 100)
                self.progress_sig.emit(pct)

            vol_data = result_flat.reshape(nx, ny, nz)
            vectors = np.diag([dx, dy, dz])

            mo_display_id = str(self.mo_idx)
            try:
                if "_" in str(self.mo_idx):
                    parts = str(self.mo_idx).split("_")
                    if parts[0].isdigit():
                        parts[0] = str(int(parts[0]) + 1)
                        mo_display_id = "_".join(parts)
                else:
                    mo_display_id = str(int(self.mo_idx) + 1)
            except Exception:
                pass

            CubeWriter.write(
                self.output_path,
                self.atoms_sym,
                self.atoms_coords,
                min_c,
                vectors,
                vol_data,
                comment=f"MO {mo_display_id}",
            )

            self.finished_sig.emit(True, self.output_path)

        except Exception as e:
            traceback.print_exc()
            self.finished_sig.emit(False, str(e))
